package proyecto;

/**
 * Enumeración que representa el estado de una asignatura.
 * @author [María Fernanda]
 */
public enum EstadoAsignatura {
	/** La asignatura ha sido superada. */
	SUPERADO,
	/** La asignatura está siendo cursada actualmente. */
	CURSADO,
	/** La asignatura ha sido abandonada. */
	ABANDONADO
}
